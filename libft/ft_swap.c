#include "libft.h"

void	ft_pswap(char **a, char **b)
{
	void	*t;

	t = *a;
	*a = *b;
	*b = t;
}

void	ft_nswap(void *a, void *b)
{
	*(int *)b ^= *(int *)a;
	*(int *)a ^= *(int *)b;
	*(int *)b ^= *(int *)a;
}
